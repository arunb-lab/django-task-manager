# taskmanager/__init__.py
#
# This file is intentionally (mostly) empty.
#
# In Python, any folder with an __init__.py file is called a "package".
# This file tells Python: "treat this folder as a package you can import from."
#
# Example: Because of this file, you can write:
#   from taskmanager import settings
#   from taskmanager.urls import urlpatterns
#
# Without __init__.py, Python wouldn't recognize 'taskmanager' as a package.
# Django's 'startproject' command creates this file automatically.
